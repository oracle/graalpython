from math import sqrt


def call_sqrt(num):
    value = 0
    for i in range(num):
        value = sqrt(i)

    return value


def measure(num):
    result = call_sqrt(num)

    print("Sqrt", result)


def __benchmark__(num=500000000):
    measure(num)
